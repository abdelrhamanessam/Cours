-- 1. COURSES
create table courses (
  id bigint generated by default as identity primary key,
  title text not null,
  image_url text default '',
  description text default '',
  icon text default '',
  level text not null default 'sec1',
  sort_order bigint default 0,
  created_at timestamptz default now()
);

-- 2. LESSONS
create table lessons (
  id bigint generated by default as identity primary key,
  course_id bigint not null references courses(id) on delete cascade,
  title text not null,
  topic text default '',
  description text default '',
  sort_order bigint default 0,
  created_at timestamptz default now()
);

-- 3. LECTURES
create table lectures (
  id bigint generated by default as identity primary key,
  lesson_id bigint not null references lessons(id) on delete cascade,
  title text not null,
  content text default '',
  math text default '',
  type text default 'Video',
  video_url text default '',
  sort_order bigint default 0,
  created_at timestamptz default now()
);

-- 4. HOMEWORK
create table homework (
  id bigint generated by default as identity primary key,
  lesson_id bigint not null references lessons(id) on delete cascade unique,
  pass_score bigint default 60
);

-- 5. HW QUESTIONS
create table hw_questions (
  id bigint generated by default as identity primary key,
  homework_id bigint not null references homework(id) on delete cascade,
  question text not null,
  options jsonb not null,
  correct bigint not null,
  sort_order bigint default 0,
  math text default ''
);

-- 6. EXAMS
create table exams (
  id bigint generated by default as identity primary key,
  lesson_id bigint not null references lessons(id) on delete cascade unique,
  pass_score bigint default 60
);

-- 7. EXAM QUESTIONS
create table exam_questions (
  id bigint generated by default as identity primary key,
  exam_id bigint not null references exams(id) on delete cascade,
  question text not null,
  options jsonb not null,
  correct bigint not null,
  sort_order bigint default 0,
  math text default ''
);

-- 8. PROFILES
create table profiles (
  id uuid primary key references auth.users(id) on delete cascade,
  name text default '',
  role text default 'student' check (role in ('admin', 'student')),
  level text not null default 'sec1',
  created_at timestamptz default now()
);

-- 9. PROGRESS
create table progress (
  id bigint generated by default as identity primary key,
  user_id uuid not null references profiles(id) on delete cascade,
  lesson_id bigint not null references lessons(id) on delete cascade,
  hw_score bigint,
  exam_score bigint,
  exam_completed boolean default false,
  hw_result jsonb,
  exam_result jsonb,
  created_at timestamptz default now(),
  updated_at timestamptz default now(),
  unique(user_id, lesson_id)
);

