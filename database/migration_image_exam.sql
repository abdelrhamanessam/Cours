-- Image-based exam questions: question + options all in one image, student picks A/B/C/D
CREATE TABLE IF NOT EXISTS exam_image_questions (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  exam_id BIGINT NOT NULL REFERENCES exams(id) ON DELETE CASCADE,
  image_url TEXT NOT NULL,
  correct TEXT NOT NULL DEFAULT 'A',
  explanation TEXT DEFAULT '',
  sort_order BIGINT DEFAULT 0
);

ALTER TABLE exam_image_questions ENABLE ROW LEVEL SECURITY;

CREATE POLICY "Admins can manage image questions"
  ON exam_image_questions FOR ALL
  TO authenticated
  USING ((SELECT role FROM profiles WHERE id = auth.uid()) = 'admin')
  WITH CHECK ((SELECT role FROM profiles WHERE id = auth.uid()) = 'admin');

CREATE POLICY "Students can read image questions"
  ON exam_image_questions FOR SELECT
  TO authenticated
  USING (true);

